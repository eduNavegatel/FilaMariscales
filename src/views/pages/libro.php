<script>
document.addEventListener('DOMContentLoaded', function() {
    const bookPages = document.querySelectorAll('.book-page');
    const chapterBtns = document.querySelectorAll('.chapter-btn');
    const prevBtn = document.getElementById('prevChapter');
    const nextBtn = document.getElementById('nextChapter');
    const currentChapter = document.getElementById('currentChapter');
    
    let currentPage = 1;
    const totalPages = bookPages.length;
    
    function showPage(pageNumber) {
        // Hide all pages
        bookPages.forEach(page => {
            page.classList.remove('active');
        });
        
        // Show current page
        const targetPage = document.querySelector(`[data-page="${pageNumber}"]`);
        if (targetPage) {
            targetPage.classList.add('active');
        }
        
        // Update navigation
        currentChapter.textContent = pageNumber;
        
        // Update chapter buttons
        chapterBtns.forEach(btn => {
            btn.classList.remove('active');
        });
        
        const targetBtn = document.querySelector(`.chapter-btn[data-page="${pageNumber}"]`);
        if (targetBtn) {
            targetBtn.classList.add('active');
        }
        
        // Update navigation buttons
        prevBtn.disabled = pageNumber === 1;
        nextBtn.disabled = pageNumber === totalPages;
        
        // Scroll to top
        window.scrollTo({ top: 0, behavior: 'smooth' });
    }
    
    // Event listeners
    prevBtn.addEventListener('click', () => {
        if (currentPage > 1) {
            currentPage--;
            showPage(currentPage);
        }
    });
    
    nextBtn.addEventListener('click', () => {
        if (currentPage < totalPages) {
            currentPage++;
            showPage(currentPage);
        }
    });
    
    chapterBtns.forEach(btn => {
        btn.addEventListener('click', () => {
            const pageNumber = parseInt(btn.dataset.page);
            currentPage = pageNumber;
            showPage(pageNumber);
        });
    });
    
    // Keyboard navigation
    document.addEventListener('keydown', (e) => {
        if (e.key === 'ArrowLeft' && currentPage > 1) {
            currentPage--;
            showPage(currentPage);
        } else if (e.key === 'ArrowRight' && currentPage < totalPages) {
            currentPage++;
            showPage(currentPage);
        }
    });
    
    // Initialize
    showPage(1);
});
</script>
